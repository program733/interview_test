def remove_list_dup(input_list):
    '''
     Objective of the function is to remove duplicates from the given list
    :param input_list: list object containing duplicate values
    :return: list without duplicate value
    '''
    # temp list to append list values
    output_list =[]
    for i in input_list:
        if i not in output_list:
            output_list.append(i)

    return output_list


